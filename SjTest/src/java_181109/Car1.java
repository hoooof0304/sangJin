package java_181109;

public class Car1 {
	
	//필드 선언
	private int speed;
	private boolean stop;
	
	
	public int getSpeed() {
		return speed;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	public boolean isStop() {
		return stop;
	}
	public void setStop(boolean stop) {
		this.stop = stop;
	}
	
	//getter / setter 자동완성 (필드 작성 후 우클릭, 소스 -> getter,setter)
}
